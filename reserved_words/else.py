"""
we use 'else' statement to execute instructions
when a condition is not True.
It also is known as a default action.
"""

color = "blue"

if color == "red":
    print("color is red")
elif color == "black":
    print("color is black")
else: 
    print("It's another color")



for i in range(9):
    print(i)
else:
    print("cycle is finished")
